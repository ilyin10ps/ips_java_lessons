package lessons.sprint_03.dz05z02;

public class Calculator {
    // Сумма:
    public static int add(int a, int b) { return a + b; }
    public static double add(double a, double b) { return a + b; }
    public static long add(long a, long b) { return a + b; }
    // Деление:
    public static int division(int a, int b) { return a / b; }
    public static double division(double a, double b) { return a / b; }
    public static long division(long a, long b) { return a / b; }
    // Умножение:
    public static int multiply(int a, int b) { return a * b; }
    public static double multiply(double a, double b) { return a * b; }
    public static long multiply(long a, long b) { return a * b; }
    // Вычитание:
    public static int minus(int a, int b) { return a - b; }
    public static double minus(double a, double b) { return a - b; }
    public static long minus(long a, long b) { return a - b; }
}